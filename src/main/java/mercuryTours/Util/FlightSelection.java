package mercuryTours.Util;

public class FlightSelection {
	private String outFlightName;
	private String outFlightDepartureTime;
	private String outFlightPrice;
	private String inFlightName;
	private String inFlightDepartureTime;
	private String inFlightPrice;
	private String taxes;
	private String totalPrice;

	/**
	 * @return the outFlightName
	 */
	public String getOutFlightName() {
		return outFlightName;
	}

	/**
	 * @param outFlightName
	 *            the outFlightName to set
	 */
	public void setOutFlightName(String outFlightName) {
		this.outFlightName = outFlightName;
	}

	/**
	 * @return the outFlightDepartureTime
	 */
	public String getOutFlightDepartureTime() {
		return outFlightDepartureTime;
	}

	/**
	 * @param outFlightDepartureTime
	 *            the outFlightDepartureTime to set
	 */
	public void setOutFlightDepartureTime(String outFlightDepartureTime) {
		this.outFlightDepartureTime = outFlightDepartureTime;
	}

	/**
	 * @return the outFlightPrice
	 */
	public String getOutFlightPrice() {
		return outFlightPrice;
	}

	/**
	 * @param outFlightPrice
	 *            the outFlightPrice to set
	 */
	public void setOutFlightPrice(String outFlightPrice) {
		this.outFlightPrice = outFlightPrice;
	}

	/**
	 * @return the inFlightName
	 */
	public String getInFlightName() {
		return inFlightName;
	}

	/**
	 * @param inFlightName
	 *            the inFlightName to set
	 */
	public void setInFlightName(String inFlightName) {
		this.inFlightName = inFlightName;
	}

	/**
	 * @return the inFlightDepartureTime
	 */
	public String getInFlightDepartureTime() {
		return inFlightDepartureTime;
	}

	/**
	 * @param inFlightDepartureTime
	 *            the inFlightDepartureTime to set
	 */
	public void setInFlightDepartureTime(String inFlightDepartureTime) {
		this.inFlightDepartureTime = inFlightDepartureTime;
	}

	/**
	 * @return the inFlightPrice
	 */
	public String getInFlightPrice() {
		return inFlightPrice;
	}

	/**
	 * @param inFlightPrice
	 *            the inFlightPrice to set
	 */
	public void setInFlightPrice(String inFlightPrice) {
		this.inFlightPrice = inFlightPrice;
	}

	public String getTaxes() {
		return taxes;
	}

	public void setTaxes(String taxes) {
		this.taxes = taxes;
	}

	public String getTotalPrice() {
		return totalPrice;
	}

	public void setTotalPrice(String totalPrice) {
		this.totalPrice = totalPrice;
	}
}
